void Skybox::render(glm::mat4 sky_view, float current_frame) {
    glDisable(GL_DEPTH_TEST);

    shader_.use();
    shader_.setMat4("view", sky_view);
    shader_.setMat4("projection", Constants::Matrices::PROJECTION);
    shader_.setVec3("lightDir", Constants::Matrices::LIGHT_POS);
    shader_.setVec3("SkyHorizon", Constants::Colors::FOG);
    shader_.setFloat("uTime", current_frame);

    glBindVertexArray(vao_);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

    glEnable(GL_DEPTH_TEST);
}
